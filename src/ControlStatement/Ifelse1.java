package ControlStatement;

public class Ifelse1 {
	public static void main(String[] args) {
		  
		int A = 50;
		int B = 20;
		
		/* If else statement syntax 
		 if(codition){
		statement 1  executed only if condition is true 
		}
		else{
		Statement 2  executed only if condition is false 
		} */
		
		if(A>B) {     // 50>20 true
			System.out.println("A is gratest No");    // this statement is written in result
		}
		else {
			System.out.println("B is greatest No");
		}
		
	}
}
