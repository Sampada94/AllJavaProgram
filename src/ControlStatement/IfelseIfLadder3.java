package ControlStatement;

public class IfelseIfLadder3 {
public static void main(String[] args) {
	
	//If else if ladder = city
	String name = "Ratnagiri";
	
	if(name=="Mumbai") {
		System.out.println("City is Mumbai");
	}
	else if(name=="Nashik") {
		System.out.println("City is Nashik");
	}
	else if(name=="Ratnagiri") {
		System.out.println("City is Ratnagiri");
	}
	else {
		System.out.println("Invalid city");
	}
}
}
