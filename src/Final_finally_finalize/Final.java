package Final_finally_finalize;

public class Final {

	public static void main(String[] args) {
		
		// final is a keyword & access modifier which is used to restriction on variables,methods.class
		//final class can not inherited to child class
		// final method means we cant not overriden by sub class
		//Once we declared as final variable we cant modified
		
		int No=100;
		System.out.println(No);

		No=200;
		System.out.println(No);
		
		final int No1=10;
		System.out.println(No1);
		
		
		
	
	}

}
