package LoopStatement;

public class ForLoop2 {
public static void main(String[] args) {
	
	for(int A = 1;A<=20;A++) {
		System.out.println(A);
	}
	
	
	System.out.println(" ");
	for(int B = 1;B<=20;B++) {
		System.out.print(" "+B);
	}
	
	
	System.out.println(" ");
	for(int i = 5;i<=15;i++) {
		System.out.println(i);
	}
	}
		
}
