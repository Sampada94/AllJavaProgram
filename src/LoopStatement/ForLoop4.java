package LoopStatement;

public class ForLoop4 {
public static void main(String[] args) {
	
	// Even no 
	// starting point =1
	// ending point = 50
	//inc by 2
	
	for(int i =50;i<=100;i=i+2) {
		System.out.println(i);
	}
	
	// odd no
	//starting point = 2
	// ending point = 50 
	
	System.out.println("  ");
	for(int j=2;j<=50;j=j+2) {
		System.out.println(j);
	}
	System.out.println("  ");
	for(int x =2 ;x<=60;x=x+2) {
		System.out.println(x);
	}
}
}
