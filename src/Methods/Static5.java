package Methods;

public class Static5 {
	
	public static void Add() {
		int P = 55;
		int Q = 66;
		int R = P+Q;
		System.out.println("Addition of two numbers R = "+R);

	}
	
	public static void Sub() {
		int K = 78;
		int L = 23;
		int M = K-L;
		System.out.println("Substraction of two numbers M = "+M);
	}
	
	public static void Multi() {
		int X = 578;
		int Y = 87;
		int Z = X*Y;
		System.out.println("Multiplication of two numbers Z = "+Z);
	}
	
	public static void Div() {
		Float E = 578f;
		Float F = 87f;
		Float G = E/F;
		System.out.println("Division of two numbers G = "+G);
	}
}
