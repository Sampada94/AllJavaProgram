package Operators;

public class Relational {
public static void main(String[] args) {
	
	int A = 50;
	int B = 20;
	
	System.out.println(A<B);      // False
	System.out.println(A>B);      // True
	System.out.println(A<=B);    // False
	System.out.println(A>=B);    // True
	System.out.println(A!=B);    //True  != not equal to
	System.out.println(A==B);    // False
}
}
